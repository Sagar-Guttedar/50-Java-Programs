
// Write a Java Program To Check Given Character is Vowel or Not | Java Boolean Operator

public class Vowels{
    public static void main(String[] args) {
        char c = 'e';

        if (c=='a' || c=='e' || c=='i' || c=='o' || c=='u'){
            System.out.println("Vowel");
        }else{
            System.out.println("Not vowel");
        }
    }
}